public class VeMayBay {

    private String MaVe;
    private String Diemdi;
    private String Diemden;
    private String LoaiVe;
    private String GioBay;
    private String NgayBay;
    private String NgayBan;
    private float GiaVe;

    public VeMayBay() {
    }

    public VeMayBay(String MaVe, String Diemdi, String Diemden, String LoaiVe, String GioBay, String NgayBay, String NgayBan, float GiaVe) {
        this.MaVe = MaVe;
        this.Diemdi = Diemdi;
        this.Diemden = Diemden;
        this.LoaiVe = LoaiVe;
        this.GioBay = GioBay;
        this.NgayBay = NgayBay;
        this.NgayBan = NgayBan;
        this.GiaVe = GiaVe;
    }

   

    /**
     * @return the MaVe
     */
    public String getMaVe() {
        return MaVe;
    }

    /**
     * @param MaVe the MaVe to set
     */
    public void setMaVe(String MaVe) {
        this.MaVe = MaVe;
    }

    /**
     * @return the Diemdi
     */
    public String getDiemdi() {
        return Diemdi;
    }

    /**
     * @param Diemdi the Diemdi to set
     */
    public void setDiemdi(String Diemdi) {
        this.Diemdi = Diemdi;
    }

    /**
     * @return the Diemden
     */
    public String getDiemden() {
        return Diemden;
    }

    /**
     * @param Diemden the Diemden to set
     */
    public void setDiemden(String Diemden) {
        this.Diemden = Diemden;
    }

    /**
     * @return the LoaiVe
     */
    public String getLoaiVe() {
        return LoaiVe;
    }

    /**
     * @param LoaiVe the LoaiVe to set
     */
    public void setLoaiVe(String LoaiVe) {
        this.LoaiVe = LoaiVe;
    }

    /**
     * @return the GioBay
     */
    public String getGioBay() {
        return GioBay;
    }

    /**
     * @param GioBay the GioBay to set
     */
    public void setGioBay(String GioBay) {
        this.GioBay = GioBay;
    }

    /**
     * @return the NgayBay
     */
    public String getNgayBay() {
        return NgayBay;
    }

    /**
     * @param NgayBay the NgayBay to set
     */
    public void setNgayBay(String NgayBay) {
        this.NgayBay = NgayBay;
    }

    /**
     * @return the NgayBan
     */
    public String getNgayBan() {
        return NgayBan;
    }

    /**
     * @param NgayBan the NgayBan to set
     */
    public void setNgayBan(String NgayBan) {
        this.NgayBan = NgayBan;
    }

    /**
     * @return the GiaVe
     */
    public float getGiaVe() {
        return GiaVe;
    }

    /**
     * @param GiaVe the GiaVe to set
     */
    public void setGiaVe(float GiaVe) {
        this.GiaVe = GiaVe;
    }

    
    
}
